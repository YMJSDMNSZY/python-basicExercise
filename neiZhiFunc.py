# enumerate函数是Python中的一个内置函数，
# 用于将一个可迭代对象转换成一个枚举对象，同时返回每个元素的索引和对应的值。
# enumerate函数的语法如下：

# enumerate(iterable, start=0)
#
# 其中，iterable参数是一个可迭代对象，比如列表、元组、字符串等；
# start参数是一个可选参数，表示索引的起始值，默认为0。
# enumerate函数返回的是一个枚举对象，每个元素都是一个元组，
# 其中第一个元素是当前元素的索引，第二个元素是当前元素本身。可以通过list函数将枚举对象转换成一个列表。
# 下面是一个简单的例子，演示了如何使用enumerate函数：

# fruits = ['apple', 'banana', 'orange']
# for index, fruit in enumerate(fruits):
#     print(index, fruit)
#
# 输出结果为：

# 0 apple
# 1 banana
# 2 orange
#
# 在这个例子中，我们使用enumerate函数来遍历列表fruits中的元素，并且在每次迭代中输出它们的索引和值。
# 需要注意的是，enumerate函数返回的是一个枚举对象，因此我们可以直接在for循环中对它进行迭代，而无需使用range函数来生成索引。

